<p>A:	Yes, we accept orders from Australia, Belgium, Canada, Denmark, Finland, France, Germany , Greece, Greenland, Hong Kong, Ireland, Japan, Korea, Luxemburg, Malaysia, New Zealand, Norway, Portugal, Spain, Sweden, Switzerland, Taiwan, United Kingdom and the United Stated directly though our website. If your country or area is not in the list, contact us. We can ship your order almost anywhere in the world.<br>
</p>
<p>4. Q: 
  Can I pay over the phone?</p>
<p>A:	Sorry, currently we don't accept any payment over phone. Please visit our website to pick your favorite items and finish your payment online.<br>
</p>
<p>5. Q:
  Is my payment on your site secure?</p>
<p>A:	All online payments are safe and secured. Our website has created a secure transaction environment. The ordering system we used is the industry standard for encryption technology-to protect your private information registered on our site. The encryption system provides you with security and peace of mind when your browser and local network supports the use of encrypted data transmissions.<br>
</p>
<p>6. Q:
  What should I do if I am getting an error when I pay with my credit card?</p>
<p>A:	If you are trying to checkout using credit card as your payment method but receiving an error, please go back and place an order again, your previous orders placed without payment should be cancelled within 24 hours. Please check with your credit card company to verify that your card is in good standing, if your three orders or more did not go through, please contact us for assistance.<br>
</p>
<p>7. Q:
  What would happen if my order did not go through but my credit card charged?</p>
<p>A:	If you did not receive an order confirmation number within 24-48 hours, your order was not completed. If your credit card is charged for the unsuccessful order, we will return funds to your credit card account. The refund will appear on to your credit card account within 24-48 hours, depending your credit card company's processing time. All successful orders that receive an order confirmation number will be charged when the item is shipped from our warehouse.<br>
</p>
<p>8. Q:
  What can I do, if I cannot place an order successfully online?</p>
<p>A:	If you encounter this problem, please try the following steps: First, please clear the cookies, restart the browser, turn off Firewall and then try again. Second, if the problem persists, please change another computer or another internet browser, such as Firefox, and then try. Or you can try to login after several hours. Last, if the above steps don't work, you can contact us.<br>
</p>
