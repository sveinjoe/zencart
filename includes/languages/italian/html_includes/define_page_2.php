<p>A:	Generally speaking, all of our items listed on the website are available. But occasionally some items may be out of order due to strong demand. If you pickup an item and pay for it, but for any reason it is not available, we will contact you as quickly as possible, and either suggest you to choose the other similar item or process a promptly refund to your account.<br>
</p>
