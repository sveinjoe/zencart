<p>A:	Please contact our customer service via email within 24 hours of purchasing. But please be advised that sometimes the items are shipped within 24 hours after the placement of the order, depending on the time you place the order the closer to the parcel pick-up time (by shipping companies), the faster the items are shipped.<br>
</p>
<p>5. Q:
