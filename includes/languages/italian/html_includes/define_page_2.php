<p>A:	Please be advised that once an order has been placed and processed, it is difficult to make modification and cancelation. If you want to change your order, please click the button contact us. We shall do our best to accommodate your situation. But once your order has been shipped, the order cannot be changed or cancelled.<br>
</p>
<p>3. Q:
