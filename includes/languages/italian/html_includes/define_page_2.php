<p>A:	The time for delivering depends on your location, the item(s) you ordered. Usually, we will arrange your shipment in 24 hours once your payment is cleared. There are several shipping methods. For more shipping information, please contact our customer service.<br>
</p>
<p>6. Q:
  How can I track my order?</p>
<p>A:	You can check the status of your order at any time by going directly to the links provided by our customer service via email. Please note that you should have the order number and email address to track the order status. We will email the tracking number to you. Please note that carrier's website may not update the records and parcel status in time.<br>
</p>
